RegisterNetEvent("hpDebug")
AddEventHandler("hpDebug", function()
	Citizen.CreateThread(function()
		while true do
			local player = GetPlayerPed(-1)
			local pos = GetEntityCoords(player)
			local heading = GetEntityHeading(player)
			local health = GetEntityHealth(player)
			local speed = GetEntitySpeed(player)

			DrawText3D(pos.x, pos.y, pos.z + 1, "X: " .. pos.x .. " Y: " .. pos.y .. " Z: " .. pos.z)
			DrawText3D(pos.x, pos.y, pos.z + 1.05, "Heading: " .. heading)
			DrawText3D(pos.x, pos.y, pos.z + 1.1, "Health: " .. health)
			DrawText3D(pos.x, pos.y, pos.z + 1.15, "Speed: " .. speed)


			local handle, object = FindFirstObject()
			local success

			CheckDistanceAndDrawText(object, pos)
			repeat 
				success, object = FindNextObject(handle)

				CheckDistanceAndDrawText(object, pos)
			until not success

			EndFindObject(handle)

			Citizen.Wait(0)
		end
	end)
end)

function CheckDistanceAndDrawText(object, pos)
	local objCoords = GetEntityCoords(object)
	local dis = Vdist(pos.x, pos.y, pos.z, objCoords.x, objCoords.y, objCoords.z)

	if dis <= 5 then
		--SetEntityAsMissionEntity(object,  false,  true)
		--DeleteObject(object)
		--GetClosestObjectOfType(x, y, z, radius, modelHash, false, p6, p7)

		DrawText3D(objCoords.x, objCoords.y, objCoords.z + 1, "Hashkey: " .. GetEntityModel(object))
	end
end

function DrawText3D(x,y,z, text)
    local onScreen,_x,_y=World3dToScreen2d(x,y,z)
    local px,py,pz=table.unpack(GetGameplayCamCoords())
    
    if onScreen then
        SetTextScale(0.2, 0.2)
        SetTextFont(0)
        SetTextProportional(1)
        -- SetTextScale(0.0, 0.55)
        SetTextColour(255, 255, 255, 255)
        SetTextDropshadow(0, 0, 0, 0, 55)
        SetTextEdge(2, 0, 0, 0, 150)
        SetTextDropShadow()
        SetTextOutline()
        SetTextEntry("STRING")
        SetTextCentre(1)
        AddTextComponentString(text)
        DrawText(_x,_y)
    end
end

